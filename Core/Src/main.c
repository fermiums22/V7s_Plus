/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();
  MX_TIM6_Init();
  /* USER CODE BEGIN 2 */
#if MOTOR_TEST_ENABLE
  MotorControl_Init(&htim3, &hadc);
  MotorControl_MoveWheelRelative(MOTOR_TEST_FORWARD_REV, MOTOR_TEST_SPEED_RPM);
  MotorControl_MoveRightWheelRelative(MOTOR_TEST_FORWARD_REV, MOTOR_TEST_SPEED_RPM);
  s_motor_test_step = 1;
  s_motor_test_deadline_ms = HAL_GetTick() + MOTOR_TEST_MOVE_TIMEOUT_MS;
#else
  FrontIrBumper_Init(&hadc, &htim2);
  CliffIr_Init();   /* cliff x4 + side IR data model; all 5 live via the 8-ch front scan */
  BaseIr_Init();    /* 5 dock/base IR-beacon receivers (polled) for homing direction */
  BumperHit_Init(); /* PB5/PE12 impact sensors on EXTI -> instant latched hit status */
  CasterOdo_Init(); /* J20 front-caster odometry: U10 OUT1 -> PD2 EXTI2 edge counter */
  IrRemote_Init(&htim6); /* NEC decode + direction on the 5 IR rx (EXTI + TIM6 us clock) */
  MotorControl_Init(&htim3, &hadc);  /* wheels: PWM TIM3 + encoders (ADC handle unused) */
  Console_Init();   /* USART1 (JP1) RX DMA ring + IRQ-driven TX ring */
  Cmd_Init();
  Cmd_Banner();
  Buzzer_Init();    /* BUZZER1 on PA11 = TIM1_CH4 (driver-owned) */
  Buzzer_Play(BUZZER_MELODY_BOOT);
#endif

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
#if MOTOR_TEST_ENABLE
    MotorControl_Task();
    MotorControlStatus motor_status;
    MotorControl_GetStatus(&motor_status);

    if (s_motor_test_step == 1)
    {
      if ((motor_status.mode == MOTOR_CONTROL_MODE_OFF) && !MotorControl_IsRightBusy())
      {
        s_motor_test_step = 2;
        s_motor_test_deadline_ms = HAL_GetTick() + MOTOR_TEST_PAUSE_MS;
      }
      else if ((int32_t)(HAL_GetTick() - s_motor_test_deadline_ms) >= 0)
      {
        MotorControl_Stop();
        s_motor_test_step = 2;
        s_motor_test_deadline_ms = HAL_GetTick() + MOTOR_TEST_PAUSE_MS;
      }
    }
    else if (s_motor_test_step == 2)
    {
      if ((int32_t)(HAL_GetTick() - s_motor_test_deadline_ms) >= 0)
      {
        MotorControl_MoveWheelRelative(MOTOR_TEST_BACKWARD_REV, MOTOR_TEST_SPEED_RPM);
        MotorControl_MoveRightWheelRelative(MOTOR_TEST_BACKWARD_REV, MOTOR_TEST_SPEED_RPM);
        s_motor_test_step = 3;
        s_motor_test_deadline_ms = HAL_GetTick() + MOTOR_TEST_MOVE_TIMEOUT_MS;
      }
    }
    else if (s_motor_test_step == 3)
    {
      if ((motor_status.mode == MOTOR_CONTROL_MODE_OFF) && !MotorControl_IsRightBusy())
      {
        s_motor_test_step = 4;
      }
      else if ((int32_t)(HAL_GetTick() - s_motor_test_deadline_ms) >= 0)
      {
        MotorControl_Stop();
        s_motor_test_step = 4;
      }
    }
#else
    FrontIrBumper_Task();
    CliffIr_Task();
    BaseIr_Task();        /* poll the 5 dock-beacon receivers (homing strength) */
    IrRemote_Task();      /* merge per-receiver NEC decodes -> command + direction */
    MotorControl_Task();  /* brakes on a latched bumper hit (checked inside) */
    {
      uint8_t rx_c; int rx_port;
      while (Console_ReadByte(&rx_c, &rx_port)) Cmd_FeedByte(rx_port, (char)rx_c);
      Cmd_StreamTask();
      Buzzer_Task();
    }
#endif
  }
  /* USER CODE END 3 */
}

